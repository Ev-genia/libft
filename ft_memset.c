#include <unistd.h>

void	*ft_memset(void *d, int c, size_t len)
{
	size_t	i;
	char	*char_d;

	char_d = d;
	i = 0;
	while (i < len)
	{
		char_d[i] = c;
		i++;
	}
	return (char_d);
}
